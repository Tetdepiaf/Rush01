#include <stdio.h>

int	*trouveles4(int *tab,int *s)
{
	int i = 0;	
	while ( tab[i] != '\0')
	{
		if(tab[i] == 1)
		{
			s[i] = 4;
		}
		else{
			s[i] = 0;
		}
		i++;
	}
	return s;
}
